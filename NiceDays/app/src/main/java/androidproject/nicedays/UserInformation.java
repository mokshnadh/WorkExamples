public class UserInformation {

    public String Name;
    public String Age;
    public String Sex;
    public String Email;
    public String PhoneNumber;

    public UserInformation(String Name, String Age, String Sex, String PhoneNumber, String Email){
        this.Name = Name;
        this.Age = Age;
        this.Sex = Sex;
        this.Email = Email;
        this.PhoneNumber = PhoneNumber;

    }

}

